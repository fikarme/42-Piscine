int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	b;

	i = 0;
	b = 1;
	if (power < 0)
		return (0);
	if (nb == 0 && power == 0)
		return (1);
	while (i < power)
	{
		b = b * nb;
		i++;
	}
	return (b);
}
